fn main() {
	//addition
	let sum = 5550 + 7310;
	println!("The sum of 5550 and 7310 is {}",sum);

	//subtraction
	let difference:f64 = 95.5 -4.3;
	println!("difference between 95.5 and 4.3 is {}",difference);

	//multiplication
	let product:u64 = 4 * 30;
	println!("the multiple of 4 and 30 is {}",product);

	//division
	let quotient:f64 = 56.7 / 32.2;
	println!("The division of 56.7 against 32.2 is {}",quotient);

	//remainder
	let remainder = 43 % 5;
	println!("the remainderof 43 against 5 = {}",remainder);
}